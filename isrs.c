/* 我们所有的异常处理中断服务程序都将指向此函数, 这会告诉我们发生了什么异常
 * 现在我们只是通过死循环来暂停系统
 * 当所有ISR被用作“锁定”机制时，它们会禁用中断，以防止IRQ的发生并破坏内核数据结构 */
void fault_handler(struct regs *r)
{
    /* 判断是否是中断号为0~31的错误 */
    if (r->int_no < 32)
    {
        /* 显示发生的异常的描述
         * 本教程中我们简单地使用一个死循环来暂停系统 */
        puts(exception_messages[r->int_no]);
        puts(" Exception. System Halted!\n");
        for (;;);
    }
}
